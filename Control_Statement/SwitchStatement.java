package Control_Statement;
public class SwitchStatement {
    
public static void main(String[] args) {
    switch(3)
    {
        case 1:
        {
            System.out.println("Rishi");
            break;

        }
        case 2:
        {
            System.out.println("Praksh");
            break;
        }
        default:
        {
            System.out.println("Kumar");
            break;
        }
    }
    
}
}